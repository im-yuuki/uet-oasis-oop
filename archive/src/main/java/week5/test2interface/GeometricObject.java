package week5.test2interface;

public interface GeometricObject {

    /**
     * Get area of the geometric object.
     * @return area
     */
    double getArea();

    /**
     * Get perimeter of the geometric object.
     * @return perimeter
     */
    double getPerimeter();

    /**
     * Get information of the geometric object.
     * @return information string
     */
    String getInfo();

}
